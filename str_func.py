'''Функция, которая принимает на вход слово/предложение и переводит все буквы в заглавные'''
def str_upper():
    words = str(input('Введите слово/предложение'))

    print(words.upper())


str_upper()


'''Создаем функцию, которая принимает на вход слово/предложение и переводит все буквы начала слов в заглавные'''
def str_title():
    words = str(input('Введите слово/предложение'))

    print(words.title())


str_title()